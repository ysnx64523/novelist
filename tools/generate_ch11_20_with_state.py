def update_state(current: int) -> None:
    write_csv(current)
    write_stats(current)
    write_role(current)
    write_reader(current)


def main() -> int:
    BODY_DIR.mkdir(parents=True, exist_ok=True)
    lengths = {}
    for ch in range(11, 21):
        text = build_chapter(ch)
        chapter_path(ch).write_text(text, encoding="utf-8", newline="\n")
        current_len = count_non_ws(text)
        if current_len < 3500 or current_len > 4500:
            raise RuntimeError(f"chapter {ch} length out of range: {current_len}")
        update_state(ch)
        lengths[ch] = current_len

    print("generated=11-20")
    print(
        "lengths="
        + ",".join(f"{ch}:{lengths[ch]}" for ch in range(11, 21))
    )
    print("state_updated=20")
    return 0


