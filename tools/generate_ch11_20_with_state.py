def status_for(row: dict, current: int) -> tuple[str, str]:
    first = int(row["首次埋设章节"])
    plan = int(row["计划回收章节"])
    actual = row["实际回收章节"]
    if actual is not None and current >= int(actual):
        return fmt_ch(int(actual)), "已回收"
    if current >= plan:
        return "", row["after_plan"]
    if current >= first:
        return "", row["after_first"]
    return "", "埋设中"


